%共轭梯度法MATLAB程序
function [x,iter]=mcg(A,b,x,ep,N)
%用途：用共轭梯度法解线性方程组Ax=b
%格式：[x,iter]=mcg(A,b,x,ep,N)  其中 A为系数矩阵, b为右端
%向量, x为初始向量(默认零向量), ep为精度(默认1e-6),N为最大迭
%代次数(默认500次), 返回参数x,iter分别为近似解向量和迭代次数
if nargin<5, N=500; end
if nargin<4, ep=1e-6; end
if nargin<3, x=zeros(size(b)); end
r=b-A*x;
for iter=1:N
   rr=r'*r;
   if iter==1
       p=r;
   else
       beta=rr/rr1;
       p=r+beta*p;
   end
   q=A*p;
   alpha=rr/(p'*q);
   x=x+alpha*p;
   r=r-alpha*q;
   rr1=rr;
   if (norm(r)<ep), break; end
end